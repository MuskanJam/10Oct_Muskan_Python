# How will you remove last object from a list?

# To remove object from a list, list.remove(obj) function is used.Using this function, we can remove the object we want which will be written in ()roundbrackets.

list1 =[2, 33, 222, 14, 25, -1]
list1.remove(-1)
print(list1)

# Then list1[-1] is last object from the list which is remove.